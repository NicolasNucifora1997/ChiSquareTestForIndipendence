    private void setObserved(){
        data.setDim(n, m);
        for(int i = 0; i < n; i++){
            for(int j = 0; j < m; j++){
                System.out.println("frame obs: " + Long.parseLong(model.getValueAt(i,j).toString()));
                data.getObs()[i][j] = Long.parseLong(model.getValueAt(i,j).toString());
                System.out.println("data obs: " + data.getObs()[i][j]);
            }
            System.out.println("");
        }
                
    }
